var addBinary = function (a, b) {
    let x = parseInt(a, 2).toString();
    let y = parseInt(b, 2).toString();

    let carry = x & y;
    let result = x ^ y;

    while (carry != 0) {
        let shiftedcarry = carry << 1;
        carry = result & shiftedcarry;
        result ^= shiftedcarry;
    }
    return result.toString(2);
}

